package com.micro.model
/**
 */
class ClientRepresentation {
    private String firstName;
    private String lastName;
    private int age;
    private long loanId;

    ClientRepresentation() {
    }

    String getFirstName() {
        return firstName
    }

    String getLastName() {
        return lastName
    }

    int getAge() {
        return age
    }

    long getLoanId() {
        return loanId
    }
}
